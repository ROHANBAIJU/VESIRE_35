def add_translations_to_json():
    """Add Hindi and Kannada translations to disease_knowledge.json"""
    
    # Load existing JSON
    json_path = Path(__file__).parent / 'data' / 'disease_knowledge.json'
    
    with open(json_path, 'r', encoding='utf-8-sig') as f:
        data = json.load(f)
    
    # Add translations to each disease
    for disease_key, disease_data in data.items():
        if disease_key in DISEASE_TRANSLATIONS:
            # Add pre-translated name and description
            disease_data['translations'] = DISEASE_TRANSLATIONS[disease_key]
        else:
            # Basic structure if no manual translation exists
            disease_data['translations'] = {
                'hi': {
                    'name': disease_data['name'],  # Keep English as fallback
                    'description': disease_data['description']
                },
                'kn': {
                    'name': disease_data['name'],
                    'description': disease_data['description']
                }
            }
    
    # Save updated JSON
    output_path = Path(__file__).parent / 'data' / 'disease_knowledge_multilingual.json'
    
    with open(output_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"‚úÖ Multilingual dataset created: {output_path}")
    print(f"üìä Total diseases: {len(data)}")
    print(f"üåê Languages: English, Hindi (hi), Kannada (kn)")
    
    # Also update the original file
    with open(json_path, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f"‚úÖ Updated original file: {json_path}")


